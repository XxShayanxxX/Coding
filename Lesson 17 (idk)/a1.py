num1 = 10 
num2 = 4 

# AND OPERATOR
print("num1 & num2", num1 & num2)

# OR OPERATOR
print("num1 | num2", num1 | num2)

# NOT OPERATOR
print("\n ~num1", ~num1)

# XOR OPERATOR
print("num1 ^ num2", num1 ^ num2)


